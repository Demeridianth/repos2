def decorator(func):
    def wrapper():
        print('before wrap')
        func()
        print('after wrap')
    return wrapper

@decorator
def my_func():
    print('this is inside the wrapper')

print(my_func())

    




        


        



    
    





    
    




    
        
    

    


            
        
        
    



 
    












    





    










    
        











































        





    

  

        












    









